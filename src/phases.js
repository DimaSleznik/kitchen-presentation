  i1: 'Каталог выглядит как раньше, но под капотом вся геометрия и детали идут через единый слой шаблонов. Картинки, материалы и цены теперь можно менять, не трогая код — продукт готов к настоящим параметрическим шкафам.',
  i2: 'Пользователь открывает нижний шкаф и тянет ползунок — ширина, число ящиков и сторона петли меняются, а шкаф перестраивается вживую прямо на экране. Первое «вау»: гибрид доказан на самом тяжёлом семействе шкафов.',
  i3: 'Каталог — это готовые пресеты, которые ставишь в проект одним кликом, а ИИ-ассистент по просьбе добавляет модули — строго в рамках правил (размеры, совместимость). Кухню можно собирать и кликами, и словами.',
  i4: 'Доступны все типы шкафов — навесные, высокие, угловые — и нестандартные размеры под заказчика в допустимых пределах. Можно собрать настоящую кухню целиком из каталога, как в боевом планнере.',
  i5: 'Каталог наполняется быстрее (ИИ готовит черновики из прайс-листа под ревью человека), а каждый шкаф получает фотореалистичное превью от нашей обученной модели. Геометрия — наша, красивая картинка — за ИИ.',
